                                <p style="text-align: justify;">A Preliminary Meeting with Four Foreign Professors was Held to Established Collaboration among RUET and Foreign University on 14<sup>th</sup> December, 2015 in the Department of Mechanical Engineering. The teachers of Mechanical Faculty were present in the meeting. &nbsp;Professor Dr. Mohd. Rafiqul Alam Beg, Vice-Chancellor of RUET was present in the collaborative meeting. The professors are from RMIT University of Australia, National University of Singapore (NUS), International Islamic University of Malaysia (IIUM) and Harbin Institute of Technology of China. Vice-Chancellor has expressed the key issues for the need of collaboration with foreign universities in the meeting to strengthen the research capability of RUET and to make research network. The foreign professors were pleased on the proposal and have given the positive consent to establish the research collaboration with RUET. They are happy and welcome to our initiatives to take move forward the campus in future. They also give a soft corner for RUET and ready to help us.</p>
