                                <p style="text-align: justify;">The 2<sup>nd</sup> International Conference on Mechanical Industrial and Materials Engineering (ICMIME 2015) organized by faculty of Mechanical Engineering is successfully held on 11-13 December, 2015. The objectives of ICMIME-2015 were to provide a platform for exchanging ideas, innovations and advancement in the field of Mechanical, Industrial and Materials Engineering and serve as a gateway for young, bright and outstanding researchers to develop network and future collaboration. Professor Dr. M N A Hawlader, Professor of International Islamic University of Malaysia (IIUM) was present as a chief guest and inaugurated the conference on 11<sup>th</sup> December, 2015. Professor Dr. Mohd. Rafiqul Alam Beg, Vice-Chancellor of RUET was present as a chief patron. Professor Dr. Mohammad Rofiqul Islam, Department of Mechanical Engineering, RUET was present as the organizing chair and delivered the welcome speech of the opening ceremony.&nbsp;The conference received an overwhelming response of a good number of researchers, academicians, practitioners and interested professionals from home and abroad to submit scientific papers and their willingness to participate in ICMIME-2015 and also in future. Upon the submission of 252 abstracts as well as full papers from all branches of Mechanical, Industrial and Materials Engineering,123 was accepted of which &nbsp;24 was from abroad. The number of keynote lectures was 04 and keynote speakers were from Australia (RMIT), Singapore (NUS), Malaysia (IIUM) and China (HIT). Participants from 4-6 countries participated in the conference. The participants were mostly researchers, engineers, scientists, academicians and professionals.&nbsp;Apart from the above, conference was decorated by research exhibitions, exciting tour. Crests, certificates and Prof. Dr. M. H. Rashid best paper award were distributed among the participants. Conference closing ceremony was held on 13 December, 2015 with a picnic tour enjoying historical place of Chapai Nawabgong. Professor Dr. Mustafizur Rahman, National University of Singapore (NUS) was present as the chief guest. Professor Dr. Mohd. Rafiqul Alam Beg, Vice-Chancellor of RUET was present as the chief patron. Professor Dr. Mohammad Rofiqul Islam, Department of Mechanical Engineering, RUET was present in the closing ceremony and delivered the welcome speech. Professor Dr. Md. Mosharraf Hossain, Department of Industrial and Production Engineering, RUET was present as the conference secretariat expressed sincere thanks to the participants from home and abroad. Before leave the Rajshahi a preliminary meeting was held with the foreign professor to established collaboration among the RUET and their university. Professor Dr. Mohd. Rafiqul Alam Beg, Vice-Chancellor of RUET was present in the collaborative meeting.&nbsp;&nbsp;</p>
