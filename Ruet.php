                                </h4>
                            </div>
                            <div class="modal-body">
                                Posted at: Sun, 20 Dec 2015 11:01:00 +0600
                                <hr>
                                <p style="text-align: justify;">Teams from RUET won the Championship in the Eco-run competition, Bangladesh was held in Bangobandhu Uddan (Bells Park), Barisal on 18<sup>th</sup> December, 2015. The competition was organized jointly by JICA (Japan International Co-operation Agency) and BHL (Bangladesh Honda Limited).&nbsp; The teams were participated from Department of Mechanical Engineering. The teams comprise with 3<sup>rd</sup> and 4<sup>th</sup> year students along with the talent faculty members of Mechanical Department. The no. of student was 23 and talent faculty members were Prof. Dr. Md. Emdadul Hoque (Head of the Department), Dr. Miah Md. Zaglul Shahadat (Assistant Prof.), Mr. Amit Roy (Assistant Prof.), Mr. Md. Monjur Morshed (Lecturer), Mr. Md. Sazon Rahman (Lecturer).</p>
<p style="text-align: justify;">&nbsp;</p>
