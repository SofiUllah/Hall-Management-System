<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;">The award contains the championship trophy and certificates. Representative of JICA (Program coordinator) handed over the award to RUETIAN Team. The names of the vehicle constructed by RUET team are RUET-1 (4 wheelers) and RUET Engenious (3 wheeler). Upon completion the competition the team of the RUET back to the campus on 19 December early morning. Note that, in 2012 the same competition was held in Dhaka and RUET team won the 1<sup>st</sup> runner up award.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="text-align: justify;"><strong>&nbsp;</strong></p>
